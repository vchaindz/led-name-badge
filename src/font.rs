/// Get the bitmap data for a character (11 bytes)
pub fn get_char_bitmap(ch: char) -> Option<&'static [u8]> {
    CHARMAP.chars().position(|c| c == ch).map(|idx| {
        let offset = idx * 11;
        &FONT_DATA[offset..offset + 11]
    })
}

