/// Get a built-in icon by name
pub fn get_icon(name: &str) -> Option<&'static Icon> {
    match name {
        "ball" => Some(&ICON_BALL),
        "happy" => Some(&ICON_HAPPY),
        "happy2" => Some(&ICON_HAPPY2),
        "heart" => Some(&ICON_HEART),
        "HEART" => Some(&ICON_HEART_FILLED),
        "heart2" => Some(&ICON_HEART2),
        "HEART2" => Some(&ICON_HEART2_FILLED),
        "fablab" => Some(&ICON_FABLAB),
        "bicycle" => Some(&ICON_BICYCLE),
        "bicycle_r" => Some(&ICON_BICYCLE_R),
        "owncloud" => Some(&ICON_OWNCLOUD),
        "on" | "power_on" | "power" => Some(&ICON_POWER_ON),
        "off" | "power_off" => Some(&ICON_POWER_OFF),
        // Status indicators
        "check" | "ok" => Some(&ICON_CHECK),
        "cross" | "x" | "error" => Some(&ICON_CROSS),
        "warn" | "warning" => Some(&ICON_WARN),
        "info" => Some(&ICON_INFO),
        // Arrows
        "left" => Some(&ICON_LEFT),
        "right" => Some(&ICON_RIGHT),
        "up" => Some(&ICON_UP),
        "down" => Some(&ICON_DOWN),
        // Communication
        "mail" | "email" => Some(&ICON_MAIL),
        "phone" => Some(&ICON_PHONE),
        "wifi" => Some(&ICON_WIFI),
        // Other symbols
        "star" => Some(&ICON_STAR),
        "lightning" | "bolt" => Some(&ICON_LIGHTNING),
        "music" | "note" => Some(&ICON_MUSIC),
        "sun" => Some(&ICON_SUN),
        "moon" => Some(&ICON_MOON),
        "coffee" => Some(&ICON_COFFEE),
        "thumbsup" | "like" => Some(&ICON_THUMBSUP),
        // Hardware
        "cpu" => Some(&ICON_CPU),
        "memory" | "ram" => Some(&ICON_MEMORY),
        "disk" | "hdd" | "ssd" => Some(&ICON_DISK),
        "gpu" => Some(&ICON_GPU),
        _ => None,
    }
}

